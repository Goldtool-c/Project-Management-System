package by.gladyshev.ProjectManagementSystem.model;

public class TaskModel {
    private int id;
    private String name;

    public TaskModel() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
